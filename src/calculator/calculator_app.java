    private void number_entry(long n){
        if(empty_flag || opr_flag || func_flag){
            num = 0;
            txt = "";
            decimal_flag = -1;
            if(end_flag){
                draft = "";
                operands.peek().clear();
                jTextField2.setText(draft);
                jTextField1.setText(txt);
            }
        }
        double x = (num < 0) ? -1 : 1;
        x = (decimal_flag >= 0) ? x*Math.pow(10, -1*(decimal_flag+1)) : 1*x;
        num = (decimal_flag >= 0) ? num + n*x : num*10 + n*x;
        if(decimal_flag >= 0) decimal_flag++;
        txt += String.valueOf(n);
        jTextField1.setText(txt);
        empty_flag = false;
        opr_flag = false;
        end_flag = false;
        func_flag = false;
        end_bracket = false;
    }
    
    private void operation_entry(char o){
        if(o == '=' && bracket_flag > 0){
            java.awt.event.ActionEvent evt = new java.awt.event.ActionEvent(this, java.awt.event.ActionEvent.ACTION_PERFORMED, "SimulateButtonClick");
            while(bracket_flag > 0){
                jButton19ActionPerformed(evt);
            }
            jButton1ActionPerformed(evt);
            return;
        }
        if(!opr_flag || end_flag){
            double n = Double.parseDouble(jTextField1.getText());
            if(operations.peek().isEmpty()){
                if(bracket_flag == 0){
                    draft = "";
                }
                jTextField2.setText(draft);
                operations.peek().push(o);
                operands.peek().push(Double.parseDouble(jTextField1.getText()));
                draft = draft + txt + " " + String.valueOf(o);
                jTextField2.setText(draft);
                opr_flag = true;
                end_flag = false;
            }
            else{
                if((o == '=' && end_bracket && bracket_flag == 0) || (end_bracket && o == ')') || (end_bracket && o != '='))
                {
                    draft = draft + " " + String.valueOf(o);
                }
                else
                {
                    draft = draft + " " + txt + " " + String.valueOf(o);
                }
                jTextField2.setText(draft);
                opr_flag = true;
                if(opr.get(o) > opr.get(operations.peek().peek())){
                    operations.peek().push(o);
                    operands.peek().push(Double.parseDouble(jTextField1.getText()));
                }
                else{
                    while(!operations.peek().isEmpty() && opr.get(o) <= opr.get(operations.peek().peek())){
                        switch(operations.peek().pop()){
                            case '+':
                                n += operands.peek().pop();
                                break;
                            case '-':
                                n = operands.peek().pop() - n;
                                break;
                            case 'x':
                                n *= operands.peek().pop();
                                break;
                            case 'รท':
                                n = operands.peek().pop() / n;
                                break;
                            case '%':
                                n = operands.peek().pop() % n;
                                break;
                            case '^':
                                n = Math.pow(operands.peek().pop(),n);
                                break;
                            case 'E':
                                n = operands.peek().pop() * Math.pow(10,n);
                                break;
                        }
                    }
                    operands.peek().push(n);
                    num = n;
                    txt = String.valueOf(n);
                    jTextField1.setText(txt);
                    operations.peek().push(o);
                }
            }
            end_flag = (o == '=');
                if(end_flag){
                    operations.peek().clear();
                    txt = String.valueOf(n);
                }
        }
        else{
            draft = draft.substring(0, draft.length()-1) + String.valueOf(o);
            jTextField2.setText(draft);
            operations.peek().pop();
            operations.peek().push(o);
            end_flag = (o == '=');
                if(end_flag){
                    operations.peek().clear();
                }
        }
    }
    
    private double factorial(double x){
        x = Math.floor(x);
        double ans = 1;
        while(x > 0){
            ans *= x;
            x--;
        }
        return ans;
    }
    
    private void keyboardInterface(java.awt.event.KeyEvent evt){
        char c = evt.getKeyChar();
        //System.out.println(e.getKeyChar() + "released");
        java.awt.event.ActionEvent e = new java.awt.event.ActionEvent(this, java.awt.event.ActionEvent.ACTION_PERFORMED, "SimulateButtonClick");
        if(c >= '0' && c <= '9'){
            if(c != '0')
            {
                number_entry(Long.parseLong(String.valueOf(c)));
            }
            else
            {
                jButton3ActionPerformed(e);
            }
        }
        else if(opr.containsKey(c)){
            operation_entry(c);
        }
        else if(c == '/' || c == '*'){
            c = (c=='/') ? 'รท' : c;
            c = (c=='*') ? 'x' : c;
            operation_entry(c);
        }
        else if(c == '('){
            jButton20ActionPerformed(e);
        }
        else if(c == '.'){
            jButton2ActionPerformed(e);
        }
        else if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            c = '=';
            operation_entry(c);
        }
        else if(evt.getKeyCode() == KeyEvent.VK_BACK_SPACE){
            jButton30ActionPerformed(e);
        }
    }
    
